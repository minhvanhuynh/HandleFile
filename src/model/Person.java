package model;

/**
 *
 * @author This PC
 */
public class Person {
    private String name;
    private String addess;
    private double salary;
    
    public Person(){}

    public Person(String name, String addess, double salary) {
        this.name = name;
        this.addess = addess;
        this.salary = salary;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getAddess() {
        return addess;
    }

    public void setAddess(String addess) {
        this.addess = addess;
    }

    public double getSalary() {
        return salary;
    }

    public void setSalary(double salary) {
        this.salary = salary;
    }

    @Override
    public String toString() {
        return String.format("%-20s%-20s%-20s", getName(), getAddess(), getSalary());
    }
    
    
    
}
